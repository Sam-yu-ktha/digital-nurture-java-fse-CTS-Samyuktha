public class Exercise07_TypeCastingExample {

    public static void main(String[] args) {

        double num1 = 45.89;

        int convertedInt = (int) num1;

        System.out.println("Double Value = " + num1);
        System.out.println("Converted Integer = " + convertedInt);

        int num2 = 25;

        double convertedDouble = num2;

        System.out.println("Integer Value = " + num2);
        System.out.println("Converted Double = " + convertedDouble);
    }
}
